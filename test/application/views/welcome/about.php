<h3 class="about-title">A BRIEF HISTORY :</h3>&nbsp;

<p class="about-des" style="text-align:justify;">Fr. Soji Olikkal, a priest of the Birmingham Archdiocese and Director of Sehion Ministries in the UK, was inspired by the Holy Spirit in March 2014 to start producing a Catholic evangelistic magazine for young people and gathered some volunteers together. With the advice of a few people who had worked in this field before, the first issue was produced at the beginning of May 2014 and was launched by Archbishop Emeritus Kevin McDonald. Five thousand copies were produced in the first month and these were distributed around the country by people who attended the May Second Saturday Convention in Birmingham. From the second month ten thousand copies were printed each month and these were distributed in the same way with additionally copies being sent by post to schools and parishes across the UK. Gradually the demand spread around the world as copies were taken to Ireland, the USA, Switzerland and Bahrain. Copies are also distributed in Australia and New Zealand. Currently 10,000 copies are printed in the UK and 2000 copies are printed for the USA.</p>

